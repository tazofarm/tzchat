// 좌측 대분류 표 생성
function createLeftTable() {
    const container = document.getElementById('leftTableContainer');
    container.innerHTML = '';
  
    const table = document.createElement('table');
  
    const thead = document.createElement('thead');
    const trHead = document.createElement('tr');
    const th = document.createElement('th');
    th.textContent = '대분류';
    trHead.appendChild(th);
    thead.appendChild(trHead);
    table.appendChild(thead);
  
    const tbody = document.createElement('tbody');
  
    area_parent.forEach((area, index) => {
      const tr = document.createElement('tr');
      tr.style.cursor = 'pointer';
      tr.onclick = () => showSubArea(index);
      const td = document.createElement('td');
      td.textContent = area.t;
      tr.appendChild(td);
      tbody.appendChild(tr);
    });
  
    table.appendChild(tbody);
    container.appendChild(table);
  }
  
  // 하위 지역 보여주기
  function showSubArea(parentIndex) {
    const container = document.getElementById('rightTableContainer');
    container.innerHTML = '';
  
    const subAreas = area_child[parentIndex];
  
    if (!subAreas || subAreas.length === 0) {
      container.innerHTML = '<p>하위 지역이 없습니다.</p>';
      return;
    }
  
    const table = document.createElement('table');
  
    const thead = document.createElement('thead');
    const trHead = document.createElement('tr');
  
    const thChk = document.createElement('th');
    thChk.textContent = '선택';
  
    const thName = document.createElement('th');
    thName.textContent = '지역명';
  
    trHead.appendChild(thChk);
    trHead.appendChild(thName);
    thead.appendChild(trHead);
    table.appendChild(thead);

// 본문 (하위 지역 리스트)
const tbody = document.createElement('tbody');

subAreas.forEach((area) => {
  const tr = document.createElement('tr');

  // 체크박스 셀
  const tdCheckbox = document.createElement('td');
  const checkbox = document.createElement('input');
  checkbox.type = 'checkbox';
  tdCheckbox.appendChild(checkbox);

  // 지역명 셀
  const tdName = document.createElement('td');
  tdName.textContent = area.t;

  tr.appendChild(tdCheckbox);
  tr.appendChild(tdName);
  tbody.appendChild(tr);
});

table.appendChild(tbody);
container.appendChild(table);
}

